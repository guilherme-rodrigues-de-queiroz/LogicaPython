#Leia o valor do raio de um círculo e calcule e imprima a área do círculo correspondente. A área do círculo circular é calculado por meio da seguinte fórmula: V = π * raio² * altura, onde π = 3.141592

raioCirc = float(input("Insira o raio do círculo: "))

area = 3.14 * (raioCirc ** 2)

print(f"A área do círculo é {area}.")